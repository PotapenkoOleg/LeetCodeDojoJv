package algo.arrays;

public class TwoPointersOppositeEnds {

    // Two pointers: One Input, Opposite Ends
    public int fn(int[] arr) {
        int left = 0;
        int right = arr.length - 1;
        int ans = 0;

        while (left < right) {
            // do some logic here with left and right
            if (1 == 1/*CONDITION*/) {
                left++;
            } else {
                right--;
            }
        }

        return ans;
    }
}
